import "@vibecodeapp/proxy"; // DO NOT REMOVE OTHERWISE VIBECODE PROXY WILL NOT WORK
import { Hono } from "hono";
import { cors } from "hono/cors";
import "./env";
import { auth } from "./auth";
import { sampleRouter } from "./routes/sample";
import { avatarsRouter } from "./routes/avatars";
import { personasRouter } from "./routes/personas";
import { contentPostsRouter } from "./routes/content-posts";
import { usersRouter } from "./routes/users";
import { generatedContentRouter } from "./routes/generated-content";
import { generateRouter } from "./routes/generate";
import { statsRouter } from "./routes/stats";
import { voiceRouter } from "./routes/voice";
import { videoRouter } from "./routes/video";
import { analyzeRouter } from "./routes/analyze";
import { publishRouter } from "./routes/publish";
import { logger } from "hono/logger";

// Type the Hono app with user/session variables
const app = new Hono<{
  Variables: {
    user: typeof auth.$Infer.Session.user | null;
    session: typeof auth.$Infer.Session.session | null;
  };
}>();

// CORS middleware - validates origin against allowlist
const allowed = [
  /^http:\/\/localhost(:\d+)?$/,
  /^http:\/\/127\.0\.0\.1(:\d+)?$/,
  /^https:\/\/[a-z0-9-]+\.dev\.vibecode\.run$/,
  /^https:\/\/[a-z0-9-]+\.vibecode\.run$/,
  /^https:\/\/[a-z0-9-]+\.vibecodeapp\.com$/,
  /^https:\/\/[a-z0-9-]+\.vibecode\.dev$/,
  /^https:\/\/vibecode\.dev$/,
];

app.use(
  "*",
  cors({
    origin: (origin) => (origin && allowed.some((re) => re.test(origin)) ? origin : null),
    credentials: true,
  })
);

// Logging
app.use("*", logger());

// Auth middleware - populates user/session for all routes
app.use("*", async (c, next) => {
  const session = await auth.api.getSession({ headers: c.req.raw.headers });
  if (!session) {
    c.set("user", null);
    c.set("session", null);
    await next();
    return;
  }
  c.set("user", session.user);
  c.set("session", session.session);
  await next();
});

// Mount auth handler
app.on(["GET", "POST"], "/api/auth/*", (c) => auth.handler(c.req.raw));

// Health check endpoint
app.get("/health", (c) => c.json({ status: "ok" }));

// Routes
app.route("/api/sample", sampleRouter);
app.route("/api/avatars", avatarsRouter);
app.route("/api/personas", personasRouter);
app.route("/api/content-posts", contentPostsRouter);
app.route("/api/users", usersRouter);
app.route("/api/generated-content", generatedContentRouter);
app.route("/api/generate", generateRouter);
app.route("/api/stats", statsRouter);
app.route("/api/voice", voiceRouter);
app.route("/api/video", videoRouter);
app.route("/api/analyze", analyzeRouter);
app.route("/api/publish", publishRouter);

const port = Number(process.env.PORT) || 3000;

export default {
  port,
  fetch: app.fetch,
};
